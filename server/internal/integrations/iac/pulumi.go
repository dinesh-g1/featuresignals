// toCamelCase converts a string to camelCase.
func toCamelCase(s string) string {
	if s == "" {
		return ""
	}
	pascal := pulumiResourceName(s)
	if len(pascal) > 0 {
		runes := []rune(pascal)
		runes[0] = unicodeToLower(runes[0])
		return string(runes)
	}
	return s
}

func unicodeToLower(r rune) rune {
	if r >= 'A' && r <= 'Z' {
		return r + 32
	}
	return r
}