package iac

import (
	"bytes"
	"context"
	"fmt"
	"strings"
	"text/template"
)

// PulumiGenerator generates Pulumi TypeScript configuration files from the
// common ResourceModel. It produces index.ts and config.ts files that can
// be used with the @featuresignals/pulumi Pulumi provider.
type PulumiGenerator struct {
	templates *template.Template
}

// NewPulumiGenerator creates a new PulumiGenerator with pre-loaded templates.
func NewPulumiGenerator() *PulumiGenerator {
	funcMap := template.FuncMap{
		"pascalCase":   pulumiResourceName,
		"quote":        func(s string) string { return fmt.Sprintf("%q", s) },
		"indent":       func(n int, s string) string { return indent(s, n) },
		"toCamelCase":  toCamelCase,
		"boolStr":      func(b bool) string { return fmt.Sprintf("%v", b) },
		"pulumiSafeKey": func(s string) string {
			r := strings.NewReplacer("-", "_", ".", "_", " ", "_")
			return r.Replace(s)
		},
	}
	tmpl := template.Must(template.New("pulumi").Funcs(funcMap).Parse(pulumiIndexTmpl + pulumiConfigTmpl))
	return &PulumiGenerator{templates: tmpl}
}

// Name returns the generator name.
func (g *PulumiGenerator) Name() string { return "pulumi" }

// FileExtension returns the file extension for generated files.
func (g *PulumiGenerator) FileExtension() string { return ".ts" }

// Generate produces Pulumi TypeScript configuration files from the resource model.
func (g *PulumiGenerator) Generate(ctx context.Context, model ResourceModel) ([]GeneratedFile, error) {
	var files []GeneratedFile

	var indexBuf bytes.Buffer
	if err := g.templates.ExecuteTemplate(&indexBuf, "index.ts", model); err != nil {
		return nil, fmt.Errorf("pulumi index template: %w", err)
	}
	files = append(files, GeneratedFile{
		Path:    "index.ts",
		Content: indexBuf.Bytes(),
		Comment: "Pulumi program for FeatureSignals resources",
	})

	var configBuf bytes.Buffer
	if err := g.templates.ExecuteTemplate(&configBuf, "config.ts", model); err != nil {
		return nil, fmt.Errorf("pulumi config template: %w", err)
	}
	files = append(files, GeneratedFile{
		Path:    "config.ts",
		Content: configBuf.Bytes(),
		Comment: "Pulumi configuration and stack references",
	})

	return files, nil
}

